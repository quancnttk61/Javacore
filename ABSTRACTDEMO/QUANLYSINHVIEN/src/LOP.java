
public class LOP {
	private String Tenlop,TenKhoa;

	public LOP(String tenlop, String tenKhoa) {
		super();
		Tenlop = tenlop;
		TenKhoa = tenKhoa;
	}

	public String getTenlop() {
		return Tenlop;
	}

	public void setTenlop(String tenlop) {
		Tenlop = tenlop;
	}

	public String getTenKhoa() {
		return TenKhoa;
	}

	public void setTenKhoa(String tenKhoa) {
		TenKhoa = tenKhoa;
	}
	

}
